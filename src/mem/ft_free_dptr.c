// libera la memoria alocada en un doble puntero de char

#include "libft.h"

void	ft_free_dptr(char **arr)
{
	int	i;

	i = 0;
	while (arr[i])
	{
		free(arr[i]);
		i++;
	}
	free(arr);
}
